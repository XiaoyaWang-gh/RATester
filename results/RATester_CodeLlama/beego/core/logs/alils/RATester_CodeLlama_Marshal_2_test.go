package alils

import (
	"bytes"
	"fmt"
	"testing"

	"github.com/gogo/protobuf/proto"
)

func TestMarshal_2(t *testing.T) {
	defer func() {
		if r := recover(); r != nil {
			fmt.Println("Recovered in main", r)
		}
	}()

	m := &LogContent{
		Key:   proto.String("key"),
		Value: proto.String("value"),
	}
	data, err := m.Marshal()
	if err != nil {
		t.Fatalf("Marshal() = %v", err)
	}
	if !bytes.Equal(data, []byte{0x0a, 0x03, 0x6b, 0x65, 0x79, 0x12, 0x03, 0x76, 0x61, 0x6c, 0x10, 0x01, 0x1a, 0x03, 0x76, 0x61, 0x6c, 0x18, 0x02, 0x20, 0x01, 0x28, 0x09, 0x52, 0x03, 0x56, 0x61, 0x6c}) {
		t.Errorf("Marshal() = %v, want %v", data, []byte{0x0a, 0x03, 0x6b, 0x65, 0x79, 0x12, 0x03, 0x76, 0x61, 0x6c, 0x10, 0x01, 0x1a, 0x03, 0x76, 0x61, 0x6c, 0x18, 0x02, 0x20, 0x01, 0x28, 0x09, 0x52, 0x03, 0x56, 0x61, 0x6c})
	}
}
